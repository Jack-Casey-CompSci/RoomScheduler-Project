    private void dropRoomButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dropRoomButtonActionPerformed
        // TODO add your handling code here:
        String room = dropRoomComboBox.getSelectedItem().toString();
        dropRoomArea.setText("Room " + room + " has been dropped.\n");
        ArrayList<ReservationEntry> allReserved = ReservationQueries.getReservationsByRoom(room);
        
        for (ReservationEntry roomEntry : allReserved) {
            WaitlistQueries.addWaitlistEntry(roomEntry.getFaculty(), roomEntry.getDate(), roomEntry.getSeats(), roomEntry.getTimestamp());
            dropRoomArea.append(roomEntry.getFaculty() + " with date " + roomEntry.getDate() + " has been added to the waitlist\n");
        }
        ReservationQueries.deleteReservationEntry(room);
        RoomQueries.dropRoom(room);
        rebuildRoomComboBoxes();
        
        ArrayList<WaitlistEntry> waitEntry = WaitlistQueries.getWaitlist();
        for (WaitlistEntry waitRoom : waitEntry) {
            ArrayList<String> allPossibleRooms = RoomQueries.getAllPossibleRooms(waitRoom.getSeats());
            ArrayList<String> reserveRooms = ReservationQueries.getRoomsReservedByDate(waitRoom.getDate());
            for (String roomCheck : allPossibleRooms) {
                if (!reserveRooms.contains(roomCheck)) {
                    ReservationQueries.addReservationEntry(waitRoom.getFaculty(), roomCheck, waitRoom.getDate(), waitRoom.getSeats(), waitRoom.getTimestamp());
                    WaitlistQueries.deleteWaitlistEntry(waitRoom.getFaculty(), waitRoom.getDate());
                    dropRoomArea.append("Faculty member " + waitRoom.getFaculty() + " has been resvered to room " + roomCheck + "\n");
                    break;
                }

            }
            

        }
        


    }//GEN-LAST:event_dropRoomButtonActionPerformed

