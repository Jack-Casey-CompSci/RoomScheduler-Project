    private void reserveSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reserveSubmitButtonActionPerformed
        // TODO add your handling code here:
        String name = reserveFacultyComboBox.getSelectedItem().toString();
        int seats = Integer.parseInt(reserveSeatsTextField.getText());
        Date date = Date.valueOf(reserveDateComboBox.getSelectedItem().toString());
        java.sql.Timestamp timestamp = new java.sql.Timestamp(Calendar.getInstance().getTime().getTime());

        ArrayList<String> allPossibleRooms = RoomQueries.getAllPossibleRooms(seats);
        ArrayList<String> reserveRooms = ReservationQueries.getRoomsReservedByDate(date);
        //going through allPossiblerooms to see if the room is in the reservations
        //if it isnt add it to reservations.
        boolean reserved = false;
        for (String room : allPossibleRooms) {
            if (!reserveRooms.contains(room)) {
                ReservationQueries.addReservationEntry(name, room, date, seats, timestamp);
                reserveStatusLabel.setText(room + " has been reserved on " + date);
                reserved = true;
                break;
            }

        }
        if (!reserved) {
            WaitlistQueries.addWaitlistEntry(name, date, seats, timestamp);
            reserveStatusLabel.setText("No availability, you have been added to the waitlist.");
        }


    }//GEN-LAST:event_reserveSubmitButtonActionPerformed

